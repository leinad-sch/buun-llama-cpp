// Load K and V decode codebooks (this TU's __constant__ copies). K = TURBO_TCQ_CB_K ?? TURBO_TCQ_CB
// ?? compiled-in; V = TURBO_TCQ_CB_V ?? TURBO_TCQ_CB ?? compiled-in. V default captured BEFORE the
// K override so K-only swaps leave V at the compiled-in anchor. Loads once via the call-site guard,
// UNLESS TURBO_TCQ_HOTSWAP is set: then the call site re-invokes and we re-read the codebook file
// whenever its mtime changes (hot-swap for sweeps, #146). Hot-swap re-reads encode AND decode so a
// swap at an empty-KV boundary stays encode/decode-consistent.
#include <sys/stat.h>
static inline void turbo_tcq_load_kv_decode() {
    auto load_file = [](const char * p, float * out) -> bool {
        FILE * f = fopen(p, "rb"); if (!f) return false;
        bool ok = fread(out, sizeof(float), 512, f) == 512; fclose(f); return ok;
    };
    auto file_mtime = [](const char * p) -> long { struct stat st; return (p && stat(p, &st) == 0) ? (long)st.st_mtime : 0; };
    int dev = 0; cudaGetDevice(&dev);
    static int hot = -1; if (hot < 0) hot = getenv("TURBO_TCQ_HOTSWAP") ? 1 : 0;
    static bool init[GGML_CUDA_MAX_DEVICES] = {};
    static long mk[GGML_CUDA_MAX_DEVICES] = {}, mv[GGML_CUDA_MAX_DEVICES] = {};
    const char * cb = getenv("TURBO_TCQ_CB");
    const char * kp = getenv("TURBO_TCQ_CB_K"); if (!kp) kp = cb;
    const char * vp = getenv("TURBO_TCQ_CB_V"); if (!vp) vp = cb;
    const bool first = !init[dev];
    const long nk = (hot || first) ? file_mtime(kp) : mk[dev];
    const long nv = (hot || first) ? file_mtime(vp) : mv[dev];
    const bool do_k = first || (hot && nk != mk[dev]);
    const bool do_v = first || (hot && nv != mv[dev]);
    if (!do_k && !do_v) return;
    float buf[512];
    if (false && first && !vp) {  // V falls back to compiled-in anchor (read before any K overwrite)
        float anchor[512]; cudaMemcpyFromSymbol(anchor, d_turbo3_tcq_codebook_fattn, 512*sizeof(float));
        cudaMemcpyToSymbol(d_turbo3_tcq_codebook_v_fattn, anchor, 512*sizeof(float)); mv[dev] = nv;
    }
    if (do_k && kp && load_file(kp, buf)) { cudaMemcpyToSymbol(d_turbo3_tcq_codebook_fattn,   buf, 512*sizeof(float)); mk[dev] = nk; }
    if (do_v && vp && load_file(vp, buf)) { cudaMemcpyToSymbol(d_turbo3_tcq_codebook_v_fattn, buf, 512*sizeof(float)); mv[dev] = nv; }
    init[dev] = true;
    if (first && (getenv("TURBO_TCQ_CB_K") || getenv("TURBO_TCQ_CB_V")))
        fprintf(stderr, "TCQ decode: K/V-split codebooks (K=%s V=%s) hotswap=%d\n", kp?kp:"compiled", vp?vp:"compiled", hot);
}

